package com.internousdev.openconnect.books.dto;

/**
 * 書籍に関するDTOクラス
 * @author SOSHI AZUMA
 * @since 2017/06/01
 * @version 1.0
 */
public class BooksDTO {

	/**
	 * ブックID
	 */
	private int bookId;

	/**
	 * タイトル
	 */
	private String title;

	/**
	 * サブタイトル
	 */
	private String subTitle;

	/**
	 * 著者
	 */
	private String author;

	/**
	 * 出版社
	 */
	private String publisher;

	/**
	 * 出版日
	 */
	private String pubDay;

	/**
	 * イニシャル
	 */
	private String initial;

	/**
	 * 状態フラグ
	 */
	private String statusFlg;

	/**
	 * カレッジ登録日
	 */
	private String regDay;

	/**
	 * 更新日
	 */
	private String updDay;

	/**
	 * 通し番号
	 */
	private int no;



	/**
	* 取得メソッド を取得
	* @return bookId
	*/
	public int getBookId() {
		return bookId;
	}

	/**
	* 設定メソッド を設定
	* @param bookId
	*/
	public void setBookId(int bookId) {
		this.bookId = bookId;
	}

	/**
	* 取得メソッド を取得
	* @return title
	*/
	public String getTitle() {
		return title;
	}

	/**
	* 設定メソッド を設定
	* @param title
	*/
	public void setTitle(String title) {
		this.title = title;
	}

	/**
	* 取得メソッド を取得
	* @return subTitle
	*/
	public String getSubTitle() {
		return subTitle;
	}

	/**
	* 設定メソッド を設定
	* @param subTitle
	*/
	public void setSubTitle(String subTitle) {
		this.subTitle = subTitle;
	}

	/**
	* 取得メソッド を取得
	* @return author
	*/
	public String getAuthor() {
		return author;
	}

	/**
	* 設定メソッド を設定
	* @param author
	*/
	public void setAuthor(String author) {
		this.author = author;
	}

	/**
	* 取得メソッド を取得
	* @return publisher
	*/
	public String getPublisher() {
		return publisher;
	}

	/**
	* 設定メソッド を設定
	* @param publisher
	*/
	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}

	/**
	* 取得メソッド を取得
	* @return pubDay
	*/
	public String getPubDay() {
		return pubDay;
	}

	/**
	* 設定メソッド を設定
	* @param pubDay
	*/
	public void setPubDay(String pubDay) {
		this.pubDay = pubDay;
	}

	/**
	* 取得メソッド を取得
	* @return initial
	*/
	public String getInitial() {
		return initial;
	}

	/**
	* 設定メソッド を設定
	* @param initial
	*/
	public void setInitial(String initial) {
		this.initial = initial;
	}

	/**
	* 取得メソッド を取得
	* @return statusFlg
	*/
	public String getStatusFlg() {
		return statusFlg;
	}

	/**
	* 設定メソッド を設定
	* @param statusFlg
	*/
	public void setStatusFlg(String statusFlg) {
		this.statusFlg = statusFlg;
	}

	/**
	* 取得メソッド を取得
	* @return regDay
	*/
	public String getRegDay() {
		return regDay;
	}

	/**
	* 設定メソッド を設定
	* @param regDay
	*/
	public void setRegDay(String regDay) {
		this.regDay = regDay;
	}

	/**
	* 取得メソッド を取得
	* @return updDay
	*/
	public String getUpdDay() {
		return updDay;
	}

	/**
	* 設定メソッド を設定
	* @param updDay
	*/
	public void setUpdDay(String updDay) {
		this.updDay = updDay;
	}

	/**
	* 取得メソッド を取得
	* @return no
	*/
	public int getNo() {
		return no;
	}

	/**
	* 設定メソッド を設定
	* @param no
	*/
	public void setNo(int no) {
		this.no = no;
	}










}
